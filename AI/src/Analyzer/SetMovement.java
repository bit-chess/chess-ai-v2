package Analyzer;

public class SetMovement {
    public int x0, xF, y0, yF;
    public String movement_;

    public SetMovement(int x0, int xF, int y0, int yF, String movement_) {
        this.x0 = x0;
        this.xF = xF;
        this.y0 = y0;
        this.yF = yF;
        this.movement_ = movement_;

    }
}
